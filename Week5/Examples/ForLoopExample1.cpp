#include <iostream>

using namespace std;

int main() {
    // Demonstrating for and while loops

    // 1. for loop: Counting from 1 to 5
    cout << "For loop: Counting from 1 to 5" << endl;
    for (int i = 1; i >= 0; i++) {
        cout << i << " ";
    }

    cout << endl;
    cout << "After the loop";


}