#include <iostream> 
// Include the iostream library for input/output operations

using namespace std; 
// Use the standard namespace for easier access to objects like cout
// If there is no using namespace cout, we would have to write std::cout
// for each time we want to use (this saves us code space)

int main() { 
  // The main function is the starting point of the program

  cout << "Hello, world!" << endl; // Print "Hello, world!" to the console 

  return 0; // Indicate successful program execution
}